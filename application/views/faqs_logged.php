		<h2>FAQ</h2>
		<h3>1. How do I create an account?</h3>
		<p><i>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;For one company/organization, only one user can first create an account. That user will be the administrator and has complete access to the full features of the application. The administrator account will fill in all necessary information, such as the company profile, and add different types of users(administrator, team leader, approving officer, regular user). </i></p><br/>
		<h3>2. Who can edit the company's profile?</h3>
		<p><i>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Only an administrator account can edit his/her company's profile. </i></p><br/>
		<h3>3. Who can add and/or delete users?</h3>
		<p><i>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;An administrator account. </i></p><br/>
		<h3>4. Who can view all requests?</h3>
		<p><i>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Only an administrator account can view all requests made by the users. An approving officer sees all requests except those made by the administrator; on the other hand, a team leader sees all requests regular users made and his requests. A regular user can only see his/her own requests. </i></p><br/>
		<h3>5. How do I create requests?</h3>
		<p><i>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Upon logging in, the user is brought to the requests page where there are table of their requests. Along with what was mentioned, the button for creating a request is also included. Just click the "Create Request" button to directed to the Create Request page. In the said page, the user can choose what type of request it will make, a reimbursement or a cash advance. After which, the user then fills in the table with details about his/her request and has an option of adding another row/s. Once done with the table, the user than has to choose which Team Leader and Approving Officer will receive and handle their request before submitting the request form. </i></p><br/>
		<h3>6. What if I want to cancel an already approved request?</h3>
		<p><i>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;The answer is <b>NO</b>. Once a request has been approved, it can no longer be cancelled.</i></p><br/>
		<h3>7. What if I want to change my request's team leader and/or approving officer?</h3>
		<p><i>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;After the submitting a request form, the team leader and approving officer chosen can no longer be changed. </i></p><br/>
			
